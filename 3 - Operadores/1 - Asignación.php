<?php


// --------------------------------
// -- Operador de asignación ~ +=, -=, %=, *=, /=, **=
// --------------------------------

$A = 220;
$B = 180;

echo "A:$A" . PHP_EOL; // 220
echo "B:$B" . PHP_EOL; // 180

$A += $B; // A = A + B
echo "A:$A" . PHP_EOL; // 400
$A -= $B; // A = A - B
echo "A:$A" . PHP_EOL; // 220
